package com.hilal;

public class Typecasting {
    public static void main(String[] args) {
        int num = (int)(5434.32f);
        System.out.println(num);
        byte a = 40;
        byte b = 50;
        byte c = 100;
        int d = a*b/c;
        System.out.println(d);

        int nums = 'a';
        System.out.println(nums);
    }
}
